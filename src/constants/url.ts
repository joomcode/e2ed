/**
 * Regexp that matches all slashes at the end of a line.
 */
export const SLASHES_AT_THE_END_REGEXP = /\/+$/;

/**
 * Regexp that matches all slashes at the start of a line.
 */
export const SLASHES_AT_THE_START_REGEXP = /^\/+/;
